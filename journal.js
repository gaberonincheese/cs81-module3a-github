// Greet a user by name
function greet(name) {
    return "Hello, " + name + "!";
}

function sayHello() {
    console.log("Hello, world!");
}

function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function determineOddOrEven(number) {
    if (number % 2 == 0) {
        console.log("Even");
    } else {
        console.log("Odd");
    }
}